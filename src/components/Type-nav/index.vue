<template>
  <div
    class="Home-nav-out"
    @mouseenter="isSearchShow = true"
    @mouseleave="isSearchShow = false"
  >
    <div class="Home-nav">
      <h2>全部商品分类</h2>
      <a href="">服装馆</a>
      <a href="">美装馆</a>
      <a href="">尚品汇超市</a>
